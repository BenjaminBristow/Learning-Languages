# if statements let your program make decisions

age = 18

if age >= 18:
    print("You are an adult")

# You can add else
if age >= 18:
    print("Adult")
else:
    print("Minor")

# You can add elif (else if)
score = 85

if score >= 90:
    print("Grade A")
elif score >= 80:
    print("Grade B")
else:
    print("Grade C or lower")