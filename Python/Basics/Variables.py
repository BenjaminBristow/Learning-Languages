# A variable stores information
# Think of it like a labeled box that holds data

name = "Alice"       # This stores a string
age = 25             # This stores an integer (whole number)
height = 5.6         # This stores a float (decimal number)
is_student = True    # This stores a boolean (True or False)

# Printing variables
print(name)
print(age)
print(height)
print(is_student)

# You can change a variable later
age = 26
print(age)